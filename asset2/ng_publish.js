function AddListenerFunctions(target) {

	// container arrays
	target.eventListeners = new Array();
	target.defaultListeners = new Array();

	// functions for setting and calling event listeners
	target.addEventListener = function(event, callback)
	{
		target.eventListeners[event] = callback;
	}
	
	target.removeEventListener = function(event)
	{
		delete this.eventListeners[event];
	}
	
	target.callEventListener = function (event, data)
	{
		if (target.eventListeners[event]) {
			target.eventListeners[event]({"event":event, "data":data});
		} else if (target.defaultListeners[event]) {
			target.defaultListeners[event](data);
		}
	}
}

